
a = 10
b = 3
# 1. Arithmetic Operators (गणित के लिए)
print("--- Arithmetic Operators ---")
print("Addition (10 + 3):", a + b)       # 13
print("Division (10 / 3):", a / b)       # 3.33 (Float)
print("Floor Division (10 // 3):", a // b) # 3 (Integer)
print("Remainder (10 % 3):", a % b)      # 1 (शेषफल)


